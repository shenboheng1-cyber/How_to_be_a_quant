# 依赖本库暂无数据/不适配的因子（计算时返回 NaN）：
#   30 用FF三因子; 143 递归SELF; 149/75/181/182 需基准指数; 165/183 SUMAC累计型
UNSUPPORTED = {30, 143, 149, 75, 181, 182, 165, 183}


def _make(i, M, bench):
    formula = FORMULAS[i]
    def fn(M_):
        if i in UNSUPPORTED:
            return pd.DataFrame(np.nan, index=M_.close.index, columns=M_.close.columns)
        code = G.translate(formula)
        return eval(code, {"__builtins__": {}}, G.namespace(M_, bench))
    return fn


def build_registry(M, bench=None) -> dict:
    """返回 {'gtja1': func, ...}，func(M)->因子矩阵。bench 为基准指数(可选)。"""
    return {f"gtja{i}": _make(i, M, bench) for i in sorted(FORMULAS)}
